  // Helper functions for searching and filtering
  export const searchHieroglyphs = (query: string): HieroglyphEntry[] => {
    const lowerQuery = query.toLowerCase();
    return hieroglyphDatabase.filter(h => 
      h.name.toLowerCase().includes(lowerQuery) ||
      h.meaning.toLowerCase().includes(lowerQuery) ||
      h.pronunciation.toLowerCase().includes(lowerQuery) ||
      h.gardinerCode.toLowerCase().includes(lowerQuery) ||
      h.category.toLowerCase().includes(lowerQuery) ||
      h.lore.toLowerCase().includes(lowerQuery)
    );
  };
  
  export const getByGardinerCode = (code: string): HieroglyphEntry | undefined => {
    return hieroglyphDatabase.find(h => h.gardinerCode.toLowerCase() === code.toLowerCase());
  };
  
  export const getByCategory = (category: string): HieroglyphEntry[] => {
    return hieroglyphDatabase.filter(h => h.category.includes(category));
  };
  
  export const getPhoneticSigns = (): HieroglyphEntry[] => {
    return hieroglyphDatabase.filter(h => h.phonetic);
  };
  
  export const getIdeograms = (): HieroglyphEntry[] => {
    return hieroglyphDatabase.filter(h => h.ideogram);
  };
  
  export const getDeterminatives = (): HieroglyphEntry[] => {
    return hieroglyphDatabase.filter(h => h.determinative);
  };
  
  export const getAllCategories = (): string[] => {
    const categories = new Set(hieroglyphDatabase.map(h => h.category));
    return Array.from(categories).sort();
  };
  
